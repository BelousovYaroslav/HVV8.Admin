    private void btnYesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnYesActionPerformed
        logger.debug( "In");
        //т.к. подэтап 2.3 ручной, мы просто переходим к следующему подэтапу (2.4)
        if( theApp.IsStepMapContainsKey( "023")) {
            TechProcessStepInfo info = theApp.GetStepInfo( "023");
            
            info.SetStopDateAsCurrent();
            info.SetStopReportTitle( "Проверка герметичности установки приборов");    
            info.SetStopP5( theApp.GetFromPoller( "005.01"));
            info.SetStopP6( theApp.GetFromPoller( "006.01"));
            info.SetStopP7( theApp.GetFromPoller( "007.01"));
                
            theApp.NextCurrentStep();
            
            if( theApp.m_pMainWnd.m_pPanel.pnlStep2.chk_02_04_AutoStart.isSelected()) {
                info = new TechProcessStepInfo( theApp);
                
                info.SetStartDateAsCurrent();
                info.SetStartReportTitle( "Старт основной откачки");    
                info.SetStartP5( theApp.GetFromPoller( "005.01"));
                info.SetStartP6( theApp.GetFromPoller( "006.01"));
                info.SetStartP7( theApp.GetFromPoller( "007.01"));
                
                theApp.SaveStepInfo( "024", info, true);
                
                //theApp.m_pMainWnd.m_EmuTimer.start();
                theApp.SetCurrentStepInProgress( true);
                StartProgramExecutor executor = new StartProgramExecutor( theApp, "AdminStep_2.4.xml");
                executor.StartThread();
            }
            
            theApp.m_ReportGenerator.Generate();
            theApp.GetStateKeeper().SaveState();
            
            theApp.m_pMainWnd.m_pPanel.SetStates();
            theApp.m_pMainWnd.m_pPanel.Reposition();
        }
        else {
            logger.fatal( "Мы заканчиваем этап 023, а инфы на него до сих пор нет!");
        }
        
        if( tWaiting != null && tWaiting.isRunning())
            tWaiting.stop();
        dispose();
        logger.debug( "Out");
    }//GEN-LAST:event_btnYesActionPerformed

