    private void btnStart_231ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStart_231ActionPerformed
        btnStart_231.setEnabled( false);
        btnStart_231.setFont( theApp.GetBoldFont());
        
        theApp.m_bSpecial231ExecFinish = false;
        if( tWaiting != null && tWaiting.isRunning())
            tWaiting.stop();
        
        tWaiting = new Timer( 1000, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                if( theApp.m_bSpecial231ExecFinish == true) {
                    tWaiting.stop();
                    lblIcoProcessing_231_2.setIcon( null);
        
                    lblStep_231.setEnabled( false);

                    lblStep_232.setEnabled( true);
                    lblStep_232.setFont( theApp.GetBoldFont());

                    btnNext.setEnabled( false);

                    btnReturn_22.setEnabled( true);
                }
            }
        });
        tWaiting.start();
        
        StartProgramExecutor executor = new StartProgramExecutor( theApp, "AdminStep_2.3.1.xml");
        executor.StartThread();
            
        lblIcoProcessing_231_2.setIcon( theApp.GetResources().getIconAnimatedProgress30());
        
        lblStep_231.setEnabled( true);
        lblStep_231.setFont( theApp.GetBoldFont());
        btnNext.setEnabled( true);
    }//GEN-LAST:event_btnStart_231ActionPerformed

    private void btnReturn_22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturn_22ActionPerformed
        theApp.SpecialCase_23();
        
        if( theApp.m_pMainWnd.m_pPanel.pnlStep2.chk_02_02_AutoStart.isSelected()) {
            TechProcessStepInfo info = new TechProcessStepInfo( theApp);

            info.SetStartDateAsCurrent();
            info.SetStartReportTitle( "Старт предварительной откачки");
            info.SetStartP5( theApp.GetFromPoller( "005.01"));
            info.SetStartP6( theApp.GetFromPoller( "006.01"));
            info.SetStartP7( theApp.GetFromPoller( "007.01"));

            theApp.SaveStepInfo( "022", info, true);

            //theApp.m_pMainWnd.m_EmuTimer.start();
            theApp.SetCurrentStepInProgress( true);
            StartProgramExecutor executor = new StartProgramExecutor( theApp, "AdminStep_2.2.xml");
            executor.StartThread();
        }
        
        theApp.m_pMainWnd.m_pPanel.SetStates();
        theApp.m_pMainWnd.m_pPanel.Reposition();
            
        if( tWaiting != null && tWaiting.isRunning())
            tWaiting.stop();
        
        dispose();
    }//GEN-LAST:event_btnReturn_22ActionPerformed

