    public void SetState() {
        if( theApp.GetCurrentStep() / 20 == 1)
            m_bCollapsed = false;
        
        //2. Установка приборов
        FillHeaderStepDates( "021", lbl_02_00_Date_start, lbl_02_00_Time_start, "024", lbl_02_00_Date_stop, lbl_02_00_Time_stop);
        
        lbl_02_00_IcoStep.setIcon( m_bCollapsed ? theApp.GetResources().getIconTriaRight() : theApp.GetResources().getIconTriaDown());
        
        lbl_02_00_Title.setFont( theApp.GetCurrentStep() / 20 == 1 ? theApp.GetBoldFont() : theApp.GetUsualFont());
        lbl_02_00_Title.setEnabled( theApp.GetCurrentStep() / 20 <= 1);
        
        
        if( m_bCollapsed == false) {
            //2.1 Занесение информации об установленных приборах
            FillStepDates( "021", lbl_02_01_Date_start, lbl_02_01_Time_start, lbl_02_01_Date_stop, lbl_02_01_Time_stop);

            lbl_02_01_Title.setFont( theApp.GetCurrentStep() == 21 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_02_01_Title.setEnabled( theApp.GetCurrentStep() <= 21);

            //РУЧНОЙ ПОДЭТАП
            btn_02_01_Next.setVisible( false); //theApp.GetCurrentStep() == 21);
            btn_02_01_Start.setVisible( false);
            
            //2.2 Предварительная откачка
            FillStepDates( "022", lbl_02_02_Date_start, lbl_02_02_Time_start, lbl_02_02_Date_stop, lbl_02_02_Time_stop);

            /*
            if( theApp.GetCurrentStep() == 22 && theApp.m_pMainWnd.m_EmuTimer.isRunning())
                lblAnimation_02_02.setIcon( theApp.GetResources().getIconAnimatedProgress25());
            else
                lblAnimation_02_02.setIcon( null);
            */
            
            lbl_02_02_Title.setFont( theApp.GetCurrentStep() == 22 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_02_02_Title.setEnabled( theApp.GetCurrentStep() <= 22);

            //chk_02_02_AutoStart.setEnabled( theApp.GetCurrentStep() < 22);
            chk_02_02_AutoStart.setEnabled( false);
            
            if( theApp.IsStepMapContainsKey( "022")) {
                TechProcessStepInfo info = theApp.GetStepInfo( "022");
                lbl_02_02_IcoStep.setIcon( info.GetExecApproved() ?  theApp.GetResources().getIconLittleBrightGreen() : null);
            }
            else
                lbl_02_02_IcoStep.setIcon( null);
            
            //АВТО-POSSIBLE ПОДЭТАП
            if( theApp.GetCurrentStep() == 22) {
                btn_02_02_Start.setVisible( !theApp.IsCurrentStepInProgress());
                btn_02_02_Next.setVisible( theApp.IsCurrentStepInProgress());
            }
            else {
                btn_02_02_Start.setVisible( false);
                btn_02_02_Next.setVisible( false);
            }
            
            //2.3 Проверка герметичности установки приборов ( ?? да   ?? нет)
            FillStepDates( "023", lbl_02_03_Date_start, lbl_02_03_Time_start, lbl_02_03_Date_stop, lbl_02_03_Time_stop);

            lbl_02_03_Title.setFont( theApp.GetCurrentStep() == 23 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_02_03_Title.setEnabled( theApp.GetCurrentStep() <= 23);

            //РУЧНОЙ ПОДЭТАП
            btn_02_03_Start.setVisible( false);
            btn_02_03_Next.setVisible( false); //theApp.GetCurrentStep() == 23);
            
            
            //2.4 Основная откачка
            FillStepDates( "024", lbl_02_04_Date_start, lbl_02_04_Time_start, lbl_02_04_Date_stop, lbl_02_04_Time_stop);

            /*
            if( theApp.GetCurrentStep() == 24 && theApp.m_pMainWnd.m_EmuTimer.isRunning())
                lblAnimation_02_04.setIcon( theApp.GetResources().getIconAnimatedProgress25());
            else
                lblAnimation_02_04.setIcon( null);
            */
            
            lbl_02_04_Title.setFont( theApp.GetCurrentStep() == 24 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_02_04_Title.setEnabled( theApp.GetCurrentStep() <= 24);

            chk_02_04_AutoStart.setEnabled( theApp.GetCurrentStep() < 24);
            
            if( theApp.IsStepMapContainsKey( "024")) {
                TechProcessStepInfo info = theApp.GetStepInfo( "024");
                lbl_02_04_IcoStep.setIcon( info.GetExecApproved() ?  theApp.GetResources().getIconLittleBrightGreen() : null);
            }
            else
                lbl_02_04_IcoStep.setIcon( null);
            
            //АВТО-POSSIBLE ПОДЭТАП
            if( theApp.GetCurrentStep() == 24) {
                btn_02_04_Start.setVisible( !theApp.IsCurrentStepInProgress());
                btn_02_04_Next.setVisible( theApp.IsCurrentStepInProgress());
            }
            else {
                btn_02_04_Start.setVisible( false);
                btn_02_04_Next.setVisible( false);
            }
        }
    }
    
    private void btn_02_02_NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_02_02_NextActionPerformed
        if( theApp.IsCurrentStepInProgress() == false) {
            //мы ещё не начинали этапа
            return;
        }
        
        //мы в процессе исполнения подэтапа 2.2 переходим к следующему подэтапу 2.3
        //theApp.m_pMainWnd.m_EmuTimer.stop();
        if( theApp.IsStepMapContainsKey( "022")) {
            TechProcessStepInfo info = theApp.GetStepInfo( "022");
            
            info.SetStopDateAsCurrent();
            info.SetStopReportTitle( "Завершение предварительной откачки");
            info.SetStopP5( theApp.GetFromPoller( "005.01"));
            info.SetStopP6( theApp.GetFromPoller( "006.01"));
            info.SetStopP7( theApp.GetFromPoller( "007.01"));

            theApp.NextCurrentStep();

            info = new TechProcessStepInfo( theApp);
            
            info.SetStartReportTitle( "");
            info.SetStartDateAsCurrent();
            info.SetStartP5( theApp.GetFromPoller( "005.01"));
            info.SetStartP6( theApp.GetFromPoller( "006.01"));
            info.SetStartP7( theApp.GetFromPoller( "007.01"));

            theApp.SaveStepInfo( "023", info, true);

            theApp.m_ReportGenerator.Generate();
            SetState();
            theApp.m_pMainWnd.m_pPanel.Reposition();

            theApp.ShowDlg2_3();
        }
        else {
            logger.fatal( "Мы заканчиваем этап 022, а инфы на него до сих пор нет!");
        }
    }//GEN-LAST:event_btn_02_02_NextActionPerformed

