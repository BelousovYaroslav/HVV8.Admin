    private void btn_02_04_NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_02_04_NextActionPerformed
        if( theApp.IsCurrentStepInProgress() == false)
            return;
        
        //мы в процессе исполнения подэтапа 2.4, переходим к следующему подэтапу 3.1
        //theApp.m_pMainWnd.m_EmuTimer.stop();
        if( theApp.IsStepMapContainsKey( "024")) {
            TechProcessStepInfo info = theApp.GetStepInfo( "024");

            info.SetStopDateAsCurrent();
            info.SetStopReportTitle( "Завершение основной откачки");
            info.SetStopP5( theApp.GetFromPoller( "005.01"));
            info.SetStopP6( theApp.GetFromPoller( "006.01"));
            info.SetStopP7( theApp.GetFromPoller( "007.01"));

            theApp.NextCurrentStep();

            if( theApp.m_pMainWnd.m_pPanel.pnlStep3.chk_03_01_AutoStart.isSelected()) {
                info = new TechProcessStepInfo( theApp);
                
                info.SetStartDateAsCurrent();
                info.SetStartReportTitle( "Старт напуска кислорода");
                info.SetStartP5( theApp.GetFromPoller( "005.01"));
                info.SetStartP6( theApp.GetFromPoller( "006.01"));
                info.SetStartP7( theApp.GetFromPoller( "007.01"));

                theApp.SaveStepInfo( "041", info, true);

                //theApp.m_pMainWnd.m_EmuTimer.start();
                StartProgramExecutor executor = new StartProgramExecutor( theApp, "AdminStep_3.1.xml");
                executor.StartThread();
                theApp.SetCurrentStepInProgress( true);
            }

            theApp.m_ReportGenerator.Generate();
            theApp.m_pMainWnd.m_pPanel.SetStates();
            theApp.m_pMainWnd.m_pPanel.Reposition();
        }
        else {
            logger.fatal( "Мы заканчиваем этап 024, а инфы на него до сих пор нет!");
        }
        
    }//GEN-LAST:event_btn_02_04_NextActionPerformed

