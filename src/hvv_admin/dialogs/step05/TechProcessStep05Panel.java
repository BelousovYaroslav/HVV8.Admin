    public void SetState() {
        if( theApp.GetCurrentStep() / 20 == m_nPageNumber)
            m_bCollapsed = false;
        
        //5. Термообезгаживание
        FillHeaderStepDates( "081", lbl_05_00_Date_start, lbl_05_00_Time_start, "085", lbl_05_00_Date_stop, lbl_05_00_Time_stop);
        
        lbl_05_00_IcoStep.setIcon( m_bCollapsed ? theApp.GetResources().getIconTriaRight() : theApp.GetResources().getIconTriaDown());
        
        lbl_05_00_Title.setFont( theApp.GetCurrentStep() / 20 == m_nPageNumber ? theApp.GetBoldFont() : theApp.GetUsualFont());
        lbl_05_00_Title.setEnabled( theApp.GetCurrentStep() / 20 <= m_nPageNumber);
        
        
        if( m_bCollapsed == false) {
            // 5.1 Установка печек (HAND STEP)
            FillStepDates( "081", lbl_05_01_Date_start, lbl_05_01_Time_start, lbl_05_01_Date_stop, lbl_05_01_Time_stop);

            lbl_05_01_Title.setFont( theApp.GetCurrentStep() == 81 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_05_01_Title.setEnabled( theApp.GetCurrentStep() <= 81);
            
            chk_05_01_AutoStart.setEnabled( theApp.GetCurrentStep() < 81);
            
            btn_05_01_Start.setVisible( false);
            btn_05_01_Next.setVisible( false);
            
            
            // 5.2 Включение PID-регулирования печек (HAND STEP)
            FillStepDates( "082", lbl_05_02_Date_start, lbl_05_02_Time_start, lbl_05_02_Date_stop, lbl_05_02_Time_stop);

            lbl_05_02_Title.setFont( theApp.GetCurrentStep() == 82 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_05_02_Title.setEnabled( theApp.GetCurrentStep() <= 82);

            btn_05_02_Start.setVisible( false);
            btn_05_02_Next.setVisible( false);
            
            
            // 5.3 Снятие печек (HAND STEP)
            FillStepDates( "083", lbl_05_03_Date_start, lbl_05_03_Time_start, lbl_05_03_Date_stop, lbl_05_03_Time_stop);

            lbl_05_03_Title.setFont( theApp.GetCurrentStep() == 83 ? theApp.GetBoldFont() : theApp.GetUsualFont());
            lbl_05_03_Title.setEnabled( theApp.GetCurrentStep() <= 83);

            chk_05_03_AutoStart.setEnabled( theApp.GetCurrentStep() < 83);
            
            btn_05_03_Start.setVisible( false);
            btn_05_03_Next.setVisible( false);
            
            
            //5.4 Заполнение рабочей смесью
            FillAutoExecutedSubStep(84,
                                    lbl_05_04_Date_start, lbl_05_04_Time_start, lblAnimation_05_04,
                                    btn_05_04_Start, lbl_05_04_Title, chk_05_04_AutoStart, lbl_05_04_IcoStep, btn_05_04_Next,
                                    lbl_05_04_Date_stop, lbl_05_04_Time_stop);
            
            //5.5 Выдержка
            FillAutoExecutedSubStep(85,
                                    lbl_05_05_Date_start, lbl_05_05_Time_start, lblAnimation_05_05,
                                    btn_05_05_Start, lbl_05_05_Title, chk_05_05_AutoStart, lbl_05_05_IcoStep, btn_05_05_Next,
                                    lbl_05_05_Date_stop, lbl_05_05_Time_stop);

        }
        
        chk_05_03_AutoStart.setEnabled( false);
    }

