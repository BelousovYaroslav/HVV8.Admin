    private void btn_05_05_NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_05_05_NextActionPerformed
        if( theApp.IsCurrentStepInProgress() == false)
            return;
        
        //мы в процессе исполнения подэтапа 5.5 "выдержка", переходим к следующему подэтапу 6.1 "Измерение ВАХ" (процесс ручной)
        //theApp.m_pMainWnd.m_EmuTimer.stop();
        if( theApp.IsStepMapContainsKey("085")) {
            TechProcessStepInfo info = theApp.GetStepInfo( "085");

            info.SetStopDateAsCurrent();
            info.SetStopReportTitle( "Завершение выдержки");
            info.SetStopP5( theApp.GetFromPoller( "005.01"));
            info.SetStopP6( theApp.GetFromPoller( "006.01"));
            info.SetStopP7( theApp.GetFromPoller( "007.01"));

            theApp.NextCurrentStep();

            info = new TechProcessStepInfo( theApp);
            info.SetStartDateAsCurrent();
            info.SetStartReportTitle( "Измерение ВАХ");
            info.SetStartP5( theApp.GetFromPoller( "005.01"));
            info.SetStartP6( theApp.GetFromPoller( "006.01"));
            info.SetStartP7( theApp.GetFromPoller( "007.01"));

            theApp.SaveStepInfo( "101", info, true);

            theApp.m_pMainWnd.m_pPanel.SetStates();
            theApp.m_ReportGenerator.Generate();
            theApp.m_pMainWnd.m_pPanel.Reposition();

            theApp.ShowDlg6_1A();
        }
        else {
            logger.fatal( "Мы заканчиваем этап 085, а инфы на него до сих пор нет!");
        }   
    }//GEN-LAST:event_btn_05_05_NextActionPerformed

