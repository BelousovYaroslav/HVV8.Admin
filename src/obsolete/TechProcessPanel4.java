            //collapsed
            
            lbl_02_01_Title.setVisible( false); nScrlBar -= 30;
            lbl_02_02_Title.setVisible( false); nScrlBar -= 30;
            lbl_02_03_Title.setVisible( false); nScrlBar -= 30;
            lbl_02_04_Title.setVisible( false); nScrlBar -= 30;
